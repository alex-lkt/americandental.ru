<h2>All-on-four or All-on-six?</h2>

<p>Most people do not understand the difference between these methods. They only know that in the All-in-four protocol, four implants are used, and in the All-in-six method, six implants are used.</p>
<p>The All-to-six protocol can be used for moderate bone atrophy, in contrast to the All-to-four method, which is indicated with a slight loss of bone.</p>
<p>The All-on-six method is low-impact, since with it the implants are installed by the puncture method, without large incisions or gum detachment.</p>
<p>The installation of implants occurs simultaneously with the removal of teeth, and the implants are placed in the wells of the extracted teeth.</p>
<p>When installing implants according to the All-on-six protocol, all layers of the bone are affected, including the zygomatic bone and the buttresses of the jaw, in contrast to the traditional methods of implantation and the All-to-four protocol. Due to this, very high rates of primary implant stability are achieved.</p>
<p>A key feature of the All-on-six method is the use of single-part implants. The implant is integral with the angled abutment.</p>
<p>Fixation of the fixed prosthesis is carried out two to three days after implantation.</p>

