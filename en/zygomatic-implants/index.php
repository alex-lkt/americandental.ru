<h2>What is zygoma zygoma implantation? </h2>
<p>
	 Zygomas differ from traditional implants in that they are placed in the zygomatic bone, and not in the upper jaw. They can be used when the quality or volume of the bone of the upper jaw is insufficient to install conventional implants. Insufficient bone in the upper jaw can be caused by bone resorption, as well as pneumatization of the maxillary sinus, or a combination of both. The minimum bone height for standard implant placement in the lateral parts of the upper jaw should be about 10 mm to ensure implant survival. With insufficient bone volume, bone grafting and sinus lifting can be performed to increase bone volume.<br>
</p>
<h3>A bit of history </h3>
<p>
	 Zygomatic implants were first introduced into clinical practice in the late 1990s by Dr. Per Ingvar Branemarck, considered the "father of implantology." The success rate of prosthetics on zygomatic implants worldwide is 97-98%. Zygomatic implants are used to restore the dentition in patients with insufficient bone in the lateral parts of the upper jaw, for example, due to aging, tumor resection, and trauma. The zygomatic bone is denser in quality and more cortical in nature than the bone of the upper jaw. Due to the good primary stability achievable in the dense zygomatic bone, and due to the uniform distribution of the load on inclined implants, the prosthesis can often be installed immediately after implantation. Zygomatic implants generally have a length of 30.0 mm to 60.0 mm.
</p>
<h2>Key points of Zygoma: </h2>
<ul>
	<li>Zygomatic implants are suitable for both the two-stage implantation protocol and the immediate loading protocol.</li>
	<li>Currently, zygomatic implants are most often used in the protocol with immediate loading of the Teeth in One Day.</li>
	<li>The main indication for the installation of zygomatic implants is severe atrophy of the bone of the upper jaw along with a complete absence of teeth, but it is also possible to use them with a partial absence of teeth</li>
	<li>Indications for the installation of zygomatic implants include: inability to perform sinus lift or bone grafting or failure during their implementation, impossibility or failure when using standard implantation protocols, rehabilitation after trauma or tumor resection</li>
</ul>
