    <h2 class="h2mob ofor">Этапы установки виниров</h2>
    <div class="row" style="margin-top:30px;">
      <div class="col-12 col-md-9">
<p style="font-weight:600">Анализ состояния зубов</p>
              <p>Для этого врач фотографирует пациента с легкой и с широкой улыбкой. По этим фотографиям врач оценивает состояние зубов и мягких тканей, чтобы понять, нужно ли их корректировать.</p>
              <p style="font-weight:600">Подготовка слепка и макета зубов</p>
              <p>Врач делает силиконовые оттиски зубов или сканирует зубы в специальном сканере. Силиконовый слепок или цифровой скан врач отсылает технику в лабораторию. Ориентируясь на силиконовый слепок, техник отливает модели зубов, поверх которых наносит реальный или «цифровой» воск. Воск помогает придать зубам нужную форму. Получается макет «идеальных» зубов (специалисты называют его wax-up) — причем техник может учесть пожелания пациента. Однако в большинстве случаев медики лучше понимают, какая форма зубов подходит к той или иной форме лица, так что стоит довериться их опыту.</p>
<p style="font-weight:600">Примерка макета на зубы</p>
              <p>Готовый макет техник отсылает врачу. Врач переносит модель в рот пациента с помощью силиконового слепка, заполненного пластмассой нужного оттенка — этот процесс называется mock-up. В результате зубы принимают такую же форму, как на макете. Если пациенту нравится результат, можно продолжать работу.
Дальнейшие шаги зависят от того, какие виниры выбрал пациент. Если композитные, то врач установит виниры в тот же визит, больше ничего с зубами не делая. Если керамические — то начнется 4 этап.</p>
<p style="font-weight:600">Препарирование зубов</p>
              <p>Препарирование нужно, чтобы появилось место под керамический винир. Врач препарирует зубы через макет — ровно на ту глубину, которую займет винир. Затем нужно будет сделать слепок или скан с обработанных зубов и отправить его в лабораторию. Изготовление постоянных керамических виниров занимает одну-три недели.
Все это время пациент будет ходить с временными винирами, которые врач изготовит и наклеит в этот же визит. Временные виниры делают из композитной пластмассы по тому же макету, который использовался для примерки.
Пластиковые виниры почти такие же удобные, как и постоянные. Почти — потому что это монолитная конструкция. Отдельные пластиковые виниры на каждый зуб никто не делает, потому что они будут слишком хрупкими.
Единственный минус пластиковых виниров — пластмасса может впитать в себя пищевые остатки и начать слегка пахнуть. Но никаких неприятных последствий для здоровья зубов за такой короткий срок не будет.</p>
<p style="font-weight:600">Установка постоянных виниров</p>
              <p>Когда постоянные керамические виниры готовы, врач приглашает пациента на заключительный визит, снимает временные виниры, примеряет и наклеивает постоянные. Если на примерке пациенту или врачу что-то не понравится, можно отправить виниры обратно технику и внести небольшие корректировки.<br>Фиксируют виниры с помощью специального прочного стоматологического клея, благодаря чему получается монолитная конструкция «винир+зуб».</p>
