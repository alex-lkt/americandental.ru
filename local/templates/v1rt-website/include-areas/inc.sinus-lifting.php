							<p>Ощущения после операции — не самые приятные: могут возникнуть отек и болезненность. Однако дискомфорт будет уменьшаться с каждым днем. Десна заживает в среднем в течение 4–7 дней (открытый синус-лифтинг). При закрытом методе заживление происходит еще быстрее. Часто возникает гематома (синяк) после синус-лифтинга, которая проходит после полного заживления. </p>
						<p style="font-weight:600;">Сколько держится отек после синус-лифтинга?</p>
							<p>Обычно выраженный отек держится несколько дней после операции (особенно ярко выражен на второй-третий день). Уменьшить неприятные ощущения можно — соблюдая послеоперационные назначения и при помощи компресса со льдом в месте оперативного вмешательства. Трудно точно спрогнозировать, когда спадет отек после синус-лифтинга. Если спустя 7–10 дней нет никаких улучшений, имеет смысл обратиться к врачу. </p>
						<p style="font-weight:600;">Что нельзя делать после синус-лифтинга?</p>
							<p> Пациенты часто спрашивают, сколько нельзя чихать после синус-лифтинга или как чихать после синус лифтинга. Желательно вообще этого не делать, особенно в первый день. Однако контролировать такие позывы трудно. Чихать нужно очень осторожно и, что важно, не закрывать при этом рот.<br>Второй вопрос по популярности — о том, сколько нельзя летать после синус-лифтинга. В течение 30 дней после операции (особенно после открытого синус-лифтинга) — как минимум, в идеале отказаться от полетов на самолете лучше на несколько недель. Если же полета не избежать, так как мы работаем с разными пациентами, рекомендуется применение препаратов против аллергии (для снижения отека тканей) и сосудосуживающих капель в нос при взлете и посадке самолета.<br>Что касается спорта после синус-лифтинга, то следует ограничить себя в физических нагрузках на 3 дня, далее допускаются умеренные физические нагрузки в течение 30 дней. </p>
