				<p><span style="font-weight:500">Высокая первичная стабильность</span><br>
							В данном случае импланты устанавливаются в костную ткань подобно винтам с эффектом компрессии, то есть происходит уплотнение костной ткани вокруг опор-стержней. Это позволяет надежно его зафиксировать даже в пористой кости.</p>
						<p><span style="font-weight:500">Между имплантом и абатментом отсутствует микрозазор</span><br>
							Одноэтапные опоры кострукционно целостны (то есть в отличие от двухэтапного, внутрикостная часть и абатмент монолитны). Это позволяет исключить скопление патогенной микрофлоры в области соединения опоры с абатментом, таким образом, снижая риск периимплантита.</p>
						<p><span style="font-weight:500">Опора-стержень и абатмент - монолитная конструкция</span><br>
							Таким образом, отсутствует риск раскрутки абатмента или выхода из строя фиксирующего винта.</p>
