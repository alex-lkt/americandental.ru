              <p>Одним из удобных видов современного съемного протезирования является <a href="/articles/byugelnye-protezy/">бюгельное</a>. В его основе – металлический каркас, к которому крепятся пластмассовые участки, имитирующие десна, на которые в свою очередь устанавливаются «зубы». Бюгельные системы можно использовать даже при расшатанных зубах, возникших как следствие пародонтоза.<span style="font-weight:600;">Металлическую дугу крепят несколькими способами:</span><p>
<ul>
    <li>С помощью кламмеров – крючков, цепляющихся к здоровым зубам.</li>
    <li>С применением аттачментов – специальных замочков, устанавливаемых один в опоре, другой – в протезной конструкции.</li>
