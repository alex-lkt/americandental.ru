<p>Решившим ставить микропротезы, нужно знать, что при их установке для идеального прилегания к поверхности зуба проводится обтачивание эмали.<br> В случае с<a href="/articles/lyuminiry/"> люминирами</a> это делается по минимуму: накладки очень тонкие.<br> Для крепления виниров обтачивается большая поверхность.<br> Если возникнет необходимость снять подобные протезы, их сразу же надо будет заменить новыми, потому что потеряется вся эстетика.</p>
<p>Следует учитывать, что <a href="/ortodontiya/">при значительном искривлении прикуса придется прибегнуть к брекетам</a>.</p>
<p>Легкие и тонкие, белые и блестящие накладки изготавливаются из <a href="/articles/keramicheskie-viniry-e-max/">керамики</a>, керамокомпозита или композита и диоксида циркония. Они не «претендуют» на большие нагрузки, «страшатся» вредных привычек, таких как курение или злоупотребление крепкими напитками, требуют бережного обращения и ухода.<br> При соблюдении всех рекомендаций виниры и люминиры будут надежно защищать эмаль от резких перепадов температур, дарить радость владельцу и прослужат более 10 лет.</p> 
