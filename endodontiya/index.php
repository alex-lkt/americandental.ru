            <p><span style="font-weight:600">Если Вы заметили болевую реакцию</span> на горячее или холодное, сладкую, кислую пищу или при надкусывании, а также страдаете от спонтанной или ночной боли - это весомый повод обратиться к стоматологу для проведения эндодонтической диагностики и последующего лечения. Кроме того, появление отечности, болезненность окружающей десны и изменение цвета зубов, являются индикаторами острого или запущенного заболевания пульпы, помощь специалиста в таких случаях необходима незамедлительно.</p>
