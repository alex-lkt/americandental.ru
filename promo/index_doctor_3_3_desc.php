<strong>Врач - стоматолог-ортопед</strong><br><br>
<p style="text-align: left;">- Один из ведущих специалистов в области челюстно-лицевой хирургии и дентальной имплантации.</p>
<p style="text-align: left;">- Пациенты отмечают дружелюбность и “легкие” руки
    доктора при удалении</p>
<p style="text-align: left;">- Специализируется на работе с деснами в области зубов
    и имплантатов: увеличение объёма, закрытие рецессии</p>